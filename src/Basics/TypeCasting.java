package Basics;

public class TypeCasting {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Value of a is " +a);
        double b = a;
        System.out.println("Value of a is " +b);

        double n = 5.55d;
        System.out.println("Value of n is " +n);
        int m =(int) n;
        System.out.println("Value of m is " +m);
    }
}
