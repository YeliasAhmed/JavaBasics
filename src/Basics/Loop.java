package Basics;

public class Loop {
    public static void main(String[] args) {


// for loop example start
        int i = 1; // i++ = i+1=2+1=3

        System.out.println("This is for loop");
        for(i=1;i<5;i++)
        {
            System.out.println(i);
        }


//    while loop start

        int j= 1;
        System.out.println("That is while loop");
        while (j<5){
            System.out.println(j);
            j++;
        }

        }


 }





